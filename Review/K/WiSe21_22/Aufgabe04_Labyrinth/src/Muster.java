public class Muster {
    private String Farbe;
    private String Muster;
    private String Geruch;

    public String getFarbe() {
        return Farbe;
    }

    public void setFarbe(String farbe) {
        Farbe = farbe;
    }

    public String getMuster() {
        return Muster;
    }

    public void setMuster(String muster) {
        Muster = muster;
    }

    public String getGeruch() {
        return Geruch;
    }

    public void setGeruch(String geruch) {
        Geruch = geruch;
    }
}
