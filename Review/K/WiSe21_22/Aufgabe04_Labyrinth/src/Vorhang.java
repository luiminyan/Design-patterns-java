public class Vorhang {
//    |
    private int x;
    private int y1;
    private int y2;

    public Vorhang(int x, int y1, int y2) {
        this.x = x;
        this.y1 = y1;
        this.y2 = y2;
    }

    public int getX() {
        return x;
    }

    public int getY1() {
        return y1;
    }

    public int getY2() {
        return y2;
    }
}
