public class ConverterFunctions {
//    类方法使用static关键字
    public static double USDtoEUR(double usd){
        return usd * 0.937;
    };
    public static double EURtoUSD(double eur){
        return eur * 1.07;
    };
    public static double CADtoUSD(double cad){
        return cad * 0.74;
    };
}
